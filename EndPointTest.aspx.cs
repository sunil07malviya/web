        protected void btnExecute_Click(object sender, System.EventArgs e)
        {
            HttpWebRequest request = CreateWebRequest();
            XmlDocument soapEnvelopeXml = new XmlDocument();
            soapEnvelopeXml.LoadXml(txtXML.Text);

            using (Stream stream = request.GetRequestStream())
            {
                soapEnvelopeXml.Save(stream);
            }

            using (WebResponse response = request.GetResponse())
            {
                using (StreamReader rd = new StreamReader(response.GetResponseStream()))
                {
                    string soapResult = rd.ReadToEnd();

                    txtResponse.Text = soapResult;
                }
            }
        }
        public HttpWebRequest CreateWebRequest()
        {
            HttpWebRequest webRequest = (HttpWebRequest)WebRequest.Create(txtEndPoint.Text);
            webRequest.Headers.Add(@"SOAP:Action");
            webRequest.ContentType = "text/xml;charset=\"utf-8\"";
            webRequest.Accept = "text/xml";
            webRequest.Method = "POST";
            if (!string.IsNullOrEmpty(txtUser.Text))
                webRequest.Credentials = new NetworkCredential(txtUser.Text, txtPassword.Text);
            webRequest.PreAuthenticate = true;
            webRequest.Timeout = System.Threading.Timeout.Infinite;
            return webRequest;
        }


